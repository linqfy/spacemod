package dev.linqfy.spacemod;

public class Planet {
    public float x, y, z;
    public float radius;
    public float colorR, colorG, colorB;

    public Planet(float x, float y, float z, float radius, float colorR, float colorG, float colorB) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.radius = radius;
        this.colorR = colorR;
        this.colorG = colorG;
        this.colorB = colorB;
    }
}
